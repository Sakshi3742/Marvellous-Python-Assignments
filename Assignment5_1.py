def main():
    no1 = int(input("Enter first number :"))
    no2 = int(input("Enter second number :"))

    sum = no1 + no2
    difference = no1 - no2
    product = no1 * no2
    division = no1 / no2

    print("Sum :",sum)
    print("Difference :",difference)
    print("Product :",product)
    print("Division :",division)

if __name__ == "__main__":
    main()